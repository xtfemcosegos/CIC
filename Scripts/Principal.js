async function VerificarSesion() {
  try {
    const archivo = await fetch('inicio_sesion.json'); // debe estar en ra√≠z
    if (!archivo.ok) throw new Error('Archivo no encontrado');

    const sesion = await archivo.json();
    const { nombre, tipo } = sesion;

    const rutaImagen = `ImgUser/${nombre}.png`;
    const img = new Image();
    img.src = rutaImagen;
    img.onerror = () => img.src = 'ImgUser/0.png';
    img.alt = 'Foto de usuario';
    img.style.height = '32px';
    img.style.borderRadius = '50%';
    img.style.marginLeft = '8px';

    const info = document.getElementById('infoUsuario');
    info.innerHTML = `üëã Bienvenido, ${nombre} (${tipo})`;
    info.appendChild(img);
  } catch (error) {
    console.log('No hay sesi√≥n activa:', error.message);
  }
}

async function IniciarSesion(event) {
  event.preventDefault();

  const usuario = document.getElementById('usuario').value.trim();
  const contrase√±a = document.getElementById('contrase√±a').value;

  if (!usuario || !contrase√±a) {
    alert('Por favor, completa todos los campos.');
    return false;
  }

  try {
    const respuesta = await fetch('https://tuservidor.com/api/login', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ usuario, contrase√±a })
    });

    if (!respuesta.ok) throw new Error(`Error: ${respuesta.status}`);

    const datos = await respuesta.json();

    if (datos.autenticado) {
      // Guardar token y datos de sesi√≥n
      localStorage.setItem('token', datos.token);

      // Crear archivo de sesi√≥n local (simulado)
      const sesion = {
        nombre: datos.nombre || usuario,
        tipo: datos.tipo || 'usuario'
      };

      // Simulaci√≥n de persistencia local (solo para entorno local)
      const blob = new Blob([JSON.stringify(sesion)], { type: 'application/json' });
      const enlace = document.createElement('a');
      enlace.href = URL.createObjectURL(blob);
      enlace.download = 'inicio_sesion.json';
      enlace.click();

      window.location.href = 'dashboard.html';
    } else {
      alert('‚ùå Usuario o contrase√±a incorrectos');
    }
  } catch (error) {
    console.error('Error al iniciar sesi√≥n:', error.message);
    alert('‚ö†Ô∏è No se pudo conectar con el servidor.');
  }

  return false;
}